    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (isFull()) {
            Vehiculo nuevo = new Vehiculo("", "", "", 0, "", "", "");
            nuevo.setPlaca(txtPlaca.getText());
            nuevo.setMarca(txtMarca.getText());
            nuevo.setModelo(txtModelo.getText());
            nuevo.setAnio(Integer.parseInt(txtAnio.getText()));
            nuevo.setColor(txtColor.getText());
            nuevo.setPrecio(txtPrecio.getText());
            nuevo.setTipoCaja(cbTipoCaja.getSelectedItem().toString());
            Registro.Vehiculo.getRegistro().modify(nuevo);
            JOptionPane.showMessageDialog(null, "El vehiculo se modifico exitosamente");
            vaciar();
            limpiartabla();
            Registro.Vehiculo.getRegistro().show(modelo);
        } else {
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (isFull()) {
            Vehiculo nuevo = new Vehiculo("", "", "", 0, "", "", "");
            nuevo.setPlaca(txtPlaca.getText());
            nuevo.setMarca(txtMarca.getText());
            nuevo.setModelo(txtModelo.getText());
            nuevo.setAnio(Integer.parseInt(txtAnio.getText()));
            nuevo.setColor(txtColor.getText());
            nuevo.setPrecio(txtPrecio.getText());
            nuevo.setTipoCaja(cbTipoCaja.getSelectedItem().toString());
            Registro.Vehiculo.getRegistro().remove(nuevo);
            JOptionPane.showMessageDialog(null, "El vehiculo se elimino exitosamente");
            vaciar();
            limpiartabla();
            Registro.Vehiculo.getRegistro().show(modelo);
        } else {
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void tableVehiculoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableVehiculoMouseClicked
        if (tableVehiculo.getSelectedRowCount() > 0) {
            txtPlaca.setText(tableVehiculo.getValueAt(tableVehiculo.getSelectedRow(), 0).toString());
            txtMarca.setText(tableVehiculo.getValueAt(tableVehiculo.getSelectedRow(), 1).toString());
            txtModelo.setText(tableVehiculo.getValueAt(tableVehiculo.getSelectedRow(), 2).toString());
            txtAnio.setText(tableVehiculo.getValueAt(tableVehiculo.getSelectedRow(), 3).toString());
            txtColor.setText(tableVehiculo.getValueAt(tableVehiculo.getSelectedRow(), 4).toString());
            txtPrecio.setText(tableVehiculo.getValueAt(tableVehiculo.getSelectedRow(), 5).toString());
        }
    }//GEN-LAST:event_tableVehiculoMouseClicked

